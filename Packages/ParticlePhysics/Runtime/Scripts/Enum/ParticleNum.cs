namespace ParticlePhysics.Enum
{
    /// <summary>
    /// Represents the available particle numbers as an enumeration.
    /// </summary>
    public enum ParticleNum
    {
        /// <param name="NUM_8K">8K particles (1024 * 8).</param>
        NUM_8K = 1024 * 8,

        /// <param name="NUM_16K">16K particles (1024 * 16).</param>
        NUM_16K = 1024 * 16,

        /// <param name="NUM_32K">32K particles (1024 * 32).</param>
        NUM_32K = 1024 * 32,

        /// <param name="NUM_64K">64K particles (1024 * 64).</param>
        NUM_64K = 1024 * 64,

        /// <param name="NUM_128K">128K particles (1024 * 128).</param>
        NUM_128K = 1024 * 128,

        /// <param name="NUM_256K">256K particles (1024 * 256).</param>
        NUM_256K = 1024 * 256
    };
}
